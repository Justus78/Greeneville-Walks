      <h2 class='info'>General</h2>
      <p>
        Commonly known as pin oak or swamp Spanish oak. This oak is a type of
        red oak and can hybridize with other red oaks such as the northern red
        oak or scarlet oaks. Flowers are monecious catkins and cannot self-pollinate,
        so the presence of another oak is required for pollination. Leaves are
        2–6.25 inches (5-16 cm) long and 2–4.25 inches (5-12 cm) broad, lobed,
        with five or seven lobes. Each lobe has five to seven bristle-tipped teeth.
        Sinuses are typically u-shaped and deep cut. Roughly the same amount of sinus
        area exists as actual leaf area. Leaves are mostly hairless, except for
        tufts of pale orange-brown down on the lower surfaces where each lobe veins
        joins the central vein. Overall autumn leaf coloration is generally bronze.
        Fruit is an acorn and are bitter to taste unless processed. The tree is
        deciduous however, young trees may retain some of their leaves through
        the winter via a natural phenomenon referred to as marcescence.
