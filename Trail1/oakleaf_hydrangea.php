      <p>
        Hydrangea quercifolia is a deciduous shrub native to the southeastern
        United States. This hydrangea is grown as an ornamental plant, with numerous
        cultivars available commercially. It is cultivated as an ornamental. Members
        of the hydrangea genus are also sometimes referred to the common name Hortenisa.
        Hydrangea quercifolia was declared the official state wildflower of Alabama in
        1999. Hydrangea quercifolia flowers are borne in erect panicles 6–12 in
        (15.2–30.5 cm) tall and 3–5 in (7.6–12.7 cm) wide at the end of branches.
        Flowers age in color from creamy white, aging to a pale pink and by autumn
        and winter are a dry, papery rusty-brown. Unlike bigleaf hydrangea
        (Hydrangea macrophylla), flower color does not vary with soil pH. The shrub
        tolerates drought, but may not flower that year if prolonged. It prefers
        slightly acidic soils with a pH of 5.0–6.5.
