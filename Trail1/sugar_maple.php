      <p>
        Besides its importance as a timber producer, the tree is best known for
        being a source of maple syrup. The tree is deciduous and during the fall
        the tree produces brightly colored foliage ranging from yellow, orange or red.
        Because of its bright foliage it is sometimes used as an ornamental. Leaves are
        3.5-5.5 inches (9-14 cm) in diameter and are deeply palmate with 5-lobes.
        Approximately as long as wide. Leaf surface is bright green above and pale green below.
        Flowers have no corolla and are perfect and staminate and appearing with leaves in crowded,
        umbel-like corymbs. Fruits appears in the autumn are dry and appear in the
        autumn on slender stems. They are dry with horseshoe-shape and slightly divergent
        wings 1.0-1.25 inches (2.5-3-6 cm) long. Twigs slender, shiny, and brownish
        with light-colored lenticels, pith is white, terminal buds (6-9 mm) long,
        acute, sharply pointed, with 4 to 8 pairs of visible scales; lateral buds
        smaller; leaf scars V-shaped; with 3 bundle scars. Bark is gray on older trees
        and deeply furrowed. Tree is capable of regenerating by stump sprout and
        root suckers. Some tree specimens have been known to attain an age of 300 to 400 years.
