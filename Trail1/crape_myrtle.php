      <h2 class='info'>General</h2>
      <p>
        A showy flowering tree that blooms in summer and are often planted as ornamentals.
        Despite the height it can attain, crepe myrtles are technically considered
        a shrub because it produces multiple trunks. Stems and branches have a mottled
        appearance due to the bark being shed throughout the year. Leaves are opposite and simple,
        with entire margins. Flowers are borne in summer on panicles of crinkled flowers
        with a waxy or crepe-like texture, hence the name. Flower colors vary among cultivars
        and range from white, to red and purple. The fruit is a capsule, green and
        succulent at first, then ripening to a dark brown. The fruit splits along six or
        seven lines, and releases numerous, small, winged seeds. Each year, the crape
        myrtles are often cut back, ostensibly to allow for new growth over the next season,
        often referred to as "crape murder", because new growth does produce more flowers than established growth.
