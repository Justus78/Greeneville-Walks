      <h2 class='info'>General</h2>
      <p>
        A small genus Liquidambar contains approximately four species, only one
        L. stryaciflua is native to the New World. Liquidambar formosana; Formosana
        sweetgum is cultivated as an ornamental in North America. Sweetgum is one
        of the most widespread trees in the southern forest. It is a desirable ornamental
        because of its attractive autumnal coloration. Maturity is reached after 200 to
        300 years. The tree is an aggressive pioneer of disturbed sites and stump sprouts are prolific.
        Leaves are simple, deciduous, 3-6 inches (7.5-15 cm) in diameter, star-shaped,
        deeply and palmately, 5-7 lobed, apex of the lobes acuminate; base typically truncate.
        Leaf surfaces are bright green above. Fall color is yellow or deep reddish purple.
        Flowers appear with the leaves. They are imperfect lacking a corolla. Staminate
        heads in racemes with indefinite stamens, pistillate terminal and solitary; pistillate
        flowers with tiny calyx and no corolla, four nonfunctional stamens, and a 2-celled ovary.
        Fruit is a woody globose head with beaked capsules about 1-1.24 inches (2.5-3 cm) in diameter.
        Bark is grayish brown. Growth is reduced on dry sites and prolonged drought may
        cause extensive dieback. Sweetgum blight is associated with protracted drought.
        The tree is an important commercial hardwood in the manufacture of plywood panels.
      </p>
