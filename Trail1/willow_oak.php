      <h2 class='info'>General</h2>
      <p>
        A popular shade tree in the United States in part due to its rapid growth rate.
        The willow oak is a medium to large tree reaching 80-100 feet high. Open-grown
        tees are distinctive with oval or oblong crown and bright green leaves. Leaves are
        deciduous but often tardy, Typical leaf length is 2-5 inches (5-13 cm) and 0.5-0.75
        inches (1-2 cm) wide. Leaf shape is narrow, linear to linear-lanceolate, margin is
        entire. Fall foliage color is yellow-brown. The tree is monoecious. Male and
        female flowers typically are present growing separately on the same branch.
        Flowers are imperfect, appearing on old and new growth. Male flowers resemble
        tassels in appearance and a called catkins. An oak catkin consists of a bract,
        a lobed calyx and pollen-producing stamens. Once the released the pollen is released,
        the entire catkin will fall from the tree. Female flowers are rarely seen by the casual
        observer, but typically appear approximately one week later than male flowers, just as
        pollen begins to be shed. The female flowers have three very small, reddish stigmas
        that rise up from the ovules located at the base of the female flower. Fruit is an
        acorn nut maturing in 1-2 seasons. Bark is smooth and steel-gray on young stems,
        ultimately developing irregular fissure and becoming nearly black on mature trees.
